# --------------------------------------------------------------------------- #
# Minimal Markdown -> HTML (only the constructs implementation-notes.md uses)
# --------------------------------------------------------------------------- #
def md_inline(text):
    text = html.escape(text)
    text = re.sub(r"`([^`]+)`", r"<code>\1</code>", text)
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", r'<a href="\2">\1</a>', text)
    return text


def md_to_html(md):
    lines = md.splitlines()
    out = []
    i = 0
    n = len(lines)
    while i < n:
        line = lines[i]

        # fenced code block
        if line.startswith("```"):
            i += 1
            buf = []
            while i < n and not lines[i].startswith("```"):
                buf.append(html.escape(lines[i]))
                i += 1
            i += 1  # closing fence
            out.append("<pre><code>" + "\n".join(buf) + "</code></pre>")
            continue

        # headings
        m = re.match(r"(#{1,3})\s+(.*)", line)
        if m:
            level = len(m.group(1))
            out.append(f"<h{level}>{md_inline(m.group(2))}</h{level}>")
            i += 1
            continue

        # unordered list (with 2-space-indented continuation lines)
        if re.match(r"[-*]\s+", line):
            items = []
            while i < n and (re.match(r"[-*]\s+", lines[i]) or (lines[i].startswith("  ") and lines[i].strip())):
                if re.match(r"[-*]\s+", lines[i]):
                    items.append(re.sub(r"[-*]\s+", "", lines[i], count=1))
                else:
                    items[-1] += " " + lines[i].strip()
                i += 1
            out.append("<ul>" + "".join(f"<li>{md_inline(t)}</li>" for t in items) + "</ul>")
            continue

        # blank line
        if not line.strip():
            i += 1
            continue

        # paragraph (gather until blank / block start)
        buf = [line]
        i += 1
        while i < n and lines[i].strip() and not re.match(r"(#{1,3}\s|[-*]\s|```)", lines[i]):
            buf.append(lines[i])
            i += 1
        out.append("<p>" + md_inline(" ".join(buf)) + "</p>")
    return "\n".join(out)


# --------------------------------------------------------------------------- #
# Inline SVG horizontal bar chart (no external libraries)
# --------------------------------------------------------------------------- #
def hbar_chart(rows, unit, fmt):
    """rows: list of (label, value, error, color). Returns an <svg> string."""
    width = 720
    row_h = 40
    pad_top = 12
    pad_bottom = 8
    label_w = 150
    val_w = 120
    bar_max = width - label_w - val_w - 20
    height = pad_top + pad_bottom + row_h * len(rows)
    vmax = max(v for _, v, _, _ in rows) or 1

    parts = [
        f'<svg viewBox="0 0 {width} {height}" role="img" xmlns="http://www.w3.org/2000/svg" font-family="-apple-system, Segoe UI, sans-serif">'
    ]
    for idx, (label, value, error, color) in enumerate(rows):
        y = pad_top + idx * row_h
        bar_y = y + 6
        bh = row_h - 16
        bw = max(2, bar_max * value / vmax)
        bx = label_w + 8
        parts.append(
            f'<text x="{label_w}" y="{bar_y + bh/2 + 4}" text-anchor="end" font-size="14" fill="#1f2937" font-weight="600">{html.escape(label)}</text>'
        )
        parts.append(
            f'<rect x="{bx}" y="{bar_y}" width="{bw:.1f}" height="{bh}" rx="4" fill="{color}"/>'
        )
        # error whisker
        if error:
            ew = bar_max * error / vmax
            ex = bx + bw
            cy = bar_y + bh / 2
            parts.append(
                f'<line x1="{ex-ew:.1f}" y1="{cy}" x2="{ex+ew:.1f}" y2="{cy}" stroke="#334155" stroke-width="1.5" opacity="0.6"/>'
            )
        parts.append(
            f'<text x="{bx + bw + 8:.1f}" y="{bar_y + bh/2 + 4}" font-size="13" fill="#475569" font-variant-numeric="tabular-nums">{fmt(value)} {unit}</text>'
        )
    parts.append("</svg>")
    return '<div class="chart">' + "".join(parts) + "</div>"


def load_jmh():
    files = sorted(glob.glob(str(REPO / "docs" / "jmh-results-*.json")))
    if not files:
        return None
    data = json.load(open(files[-1]))
    end_to_end = {}   # (engine, scenario) -> (score, error)
    construction = {}  # (bench, scenario) -> (score, error)
    for r in data:
        name = r["benchmark"].split(".")[-1]
        p = r.get("params", {})
        pm = r["primaryMetric"]
        if name == "search":
            end_to_end[(p["engine"], p["scenario"])] = (pm["score"], pm["scoreError"])
        else:
            construction[(name, p["scenario"])] = (pm["score"], pm["scoreError"])
    return {"file": Path(files[-1]).name, "end_to_end": end_to_end, "construction": construction}


def build_benchmarks_page():
    jmh = load_jmh()
    if not jmh:
        return None

    def e2e_rows(scenario, divisor):
        rows = []
        for engine in ["jdbc", "jpa", "querydsl", "jooq", "mybatis"]:
            score, err = jmh["end_to_end"][(engine, scenario)]
            rows.append((engine, score / divisor, err / divisor, ENGINE_COLORS[engine]))
        rows.sort(key=lambda r: r[1])  # fastest first
        return rows

    def con_rows(scenario):
        rows = []
        label_map = {"jdbcStringAssembly": "jdbc", "jooqAstRender": "jooq", "mybatisDynamicSqlRender": "mybatis"}
        for bench, engine in label_map.items():
            score, err = jmh["construction"][(bench, scenario)]
            rows.append((engine, score, err, ENGINE_COLORS[engine]))
        rows.sort(key=lambda r: r[1])
        return rows

    def fmt1(v):
        return f"{v:,.1f}"

    legend = '<div class="legend">' + "".join(
        f'<span><i style="background:{c}"></i>{e}</span>' for e, c in ENGINE_COLORS.items()
    ) + "</div>"

    # full table
    table_rows = ""
    for engine in ["jdbc", "jpa", "querydsl", "jooq", "mybatis"]:
        s = jmh["end_to_end"][(engine, "simple")][0]
        c = jmh["end_to_end"][(engine, "complex")][0]
        table_rows += f'<tr><td>{engine}</td><td class="num">{s:,.1f}</td><td class="num">{c:,.1f}</td></tr>'

    body = f"""<h1>Benchmarks</h1>
<p class="lede">JMH, measured against a 100k-row PostgreSQL. Lower is faster.
Numbers below are from <code>{html.escape(jmh['file'])}</code> — a captured run on
Apple Silicon under a Podman VM, so read the shape, not the absolute values.</p>
{legend}

<section class="card">
<h2>End-to-end latency &mdash; simple query</h2>
<p>One predicate, default sort. Build + execute (count + data) + map, per call.</p>
{hbar_chart(e2e_rows("simple", 1), "&micro;s", fmt1)}
</section>

<section class="card">
<h2>End-to-end latency &mdash; complex query</h2>
<p>Eight predicates + <code>IN</code> + <code>ILIKE</code> + custom sort + page 3.
Shown in milliseconds.</p>
{hbar_chart(e2e_rows("complex", 1000), "ms", fmt1)}
<div class="note"><strong>The surprise:</strong> raw JDBC and jOOQ are the
<em>slowest</em> here, not fastest. It isn't framework overhead &mdash; it's the
query plan. Those two emit <code>ILIKE</code>, which the <code>gin_trgm_ops</code>
index serves; the pattern <code>%ref%</code> matches a third of the table, so the
bitmap index scan + recheck loses to the plain sequential scan the
<code>lower() LIKE</code> engines get. The API you pick shapes the SQL, the SQL
shapes the plan, and the plan is what you pay for.</div>
</section>

<section class="card">
<h2>Query construction only &mdash; no database</h2>
<p>SQL assembly / rendering for the three engines whose SQL is separable from
execution. Nanoseconds per operation, complex scenario.</p>
{hbar_chart(con_rows("complex"), "ns", fmt1)}
<div class="note"><strong>The headline:</strong> construction costs
nanoseconds&ndash;microseconds while a database round trip costs hundreds of
microseconds to milliseconds &mdash; a 1000&times;+ gap. &ldquo;DSL overhead&rdquo;
is noise in any I/O-bound workload. Choose an abstraction for safety and
maintainability, not construction speed.</div>
</section>

<section class="card">
<h2>All end-to-end numbers</h2>
<table>
<tr><th>engine</th><th class="num">simple (&micro;s/op)</th><th class="num">complex (&micro;s/op)</th></tr>
{table_rows}
</table>
<p>Reproduce locally with <code>./mvnw -pl query-benchmarks exec:exec</code>
(≈6&nbsp;min; writes <code>query-benchmarks/results/jmh.json</code>).</p>
</section>
"""
    return page("Benchmarks — safe-query-lab", body)


