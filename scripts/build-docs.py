def build_notes():
    md = (REPO / "docs" / "implementation-notes.md").read_text()
    # Drop the leading H1; the page chrome supplies the title context.
    rendered = md_to_html(md)
    return page("Implementation notes — safe-query-lab", rendered)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--apidocs", required=True, help="path to aggregate apidocs dir")
    ap.add_argument("--out", default="site", help="output site directory")
    args = ap.parse_args()

    out = Path(args.out)
    if out.exists():
        shutil.rmtree(out)
    out.mkdir(parents=True)

    (out / "docs.css").write_text(SHARED_CSS)
    (out / "index.html").write_text(build_index())
    (out / "implementation-notes.html").write_text(build_notes())

    bench = build_benchmarks_page()
    if bench:
        (out / "benchmarks.html").write_text(bench)
        print("wrote benchmarks.html")
    else:
        print("WARNING: no docs/jmh-results-*.json found; skipping benchmarks.html")

    apidocs = Path(args.apidocs)
    if apidocs.is_dir():
        shutil.copytree(apidocs, out / "apidocs")
        print(f"copied {apidocs} -> {out/'apidocs'}")
    else:
        print(f"WARNING: apidocs dir {apidocs} not found; site will lack Javadocs")

    print(f"site assembled at {out}/")


if __name__ == "__main__":
    main()
